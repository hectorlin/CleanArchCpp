// double 示例 / Example
#include <iostream>
int main() {
    double x = 3.14159; // 双精度浮点数 / Double precision floating point
    double y = 2.71828;
    double result = x * y;
    std::cout << "x = " << x << std::endl;
    std::cout << "y = " << y << std::endl;
    std::cout << "result = " << result << std::endl;
    return 0;
} 