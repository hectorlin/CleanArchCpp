// float 示例 / Example
#include <iostream>
int main() {
    float x = 3.14f; // 单精度浮点数 / Single precision floating point
    float y = 2.71f;
    float result = x * y;
    std::cout << "x = " << x << std::endl;
    std::cout << "y = " << y << std::endl;
    std::cout << "result = " << result << std::endl;
    return 0;
} 