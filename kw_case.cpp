// case 示例 / Example
#include <iostream>
int main() {
    int n = 2;
    switch (n) {
        case 1:
            std::cout << "case 1" << std::endl;
            break;
        case 2:
            std::cout << "case 2" << std::endl;
            break;
        default:
            std::cout << "default" << std::endl;
    }
    return 0;
} 